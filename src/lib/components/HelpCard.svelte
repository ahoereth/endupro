<section class="card help-card">
  <h2>Explanations</h2>
  <ul class="help-list">
    <li>
      <strong>Running Tolerance</strong> compares your 7-day load to the longer-term
      90-day baseline and +10% cap.
    </li>
    <li>
      <strong>Fundamentals</strong> summarizes distribution patterns in distance,
      pace, duration, weekday, start time, and heart-rate zones.
    </li>
    <li>
      <strong>Heart Rate vs Pace</strong> plots interval points first, then split
      points, then run averages when finer detail is missing. Same pace, lower HR
      is better.
    </li>
    <li>
      <strong>Comparable runs</strong> limits the scatter plot to runs similar to
      the selected run or latest run in range.
    </li>
    <li>
      <strong>Pace-Bin HR Heatmap</strong> shows weekly average HR by pace bucket.
      Lower HR at the same pace is better.
    </li>
    <li>
      <strong>Pace range</strong> narrows the scatter plot to the selected pace window.
    </li>
    <li>
      <strong>Date color saturation</strong> and
      <strong>HR color saturation</strong>
      set the color scale range for the scatter plot and heatmap.
    </li>
    <li>
      <strong>Activity Details</strong> provide baseline context plus interval and
      split tables.
    </li>
  </ul>
</section>
